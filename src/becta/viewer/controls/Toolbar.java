    public void updateForThemeChanged(Font font) {

    	if(accessibilityMenu!=null){
    		
    		for(int i=0;i<accessibilityMenu.getComponentCount();++i){
    			
    			JComponent menuItem = (JComponent)accessibilityMenu.getComponent(i);
    			String text = menuItem instanceof JRadioButtonMenuItem ? ((JRadioButtonMenuItem)menuItem).getText() : ((JLabel)menuItem).getText();
    			
    			if(menuItem != null){
    				
    				menuItem.setFont(font);
    				setAccessibilityMenuItemSize(menuItem, text);
    			}
    		}
    	}		
    }
    
    /**
     * Set preferred size for accessibility menu item based on text size
     * @param menuItem
     * @param text
     */
    private void setAccessibilityMenuItemSize(JComponent menuItem, String text){
    
    	FontMetrics metrics = menuItem.getFontMetrics(menuItem.getFont());
		int width = metrics.stringWidth(text) + 15;
		int height = metrics.getHeight() + 5;
		menuItem.setPreferredSize(new Dimension(width < 205 ? 205 : width, height < 26 ? 26 : height));
    }
    
    /**
     * Fires float change event
     */
    private static void fireFloatChanged() {
        // Guaranteed to return a non-null array
        Object[] listeners = listenerList.getListenerList();
        // Process the listeners last to first, notifying
        // those that are interested in this event
        for (int i = listeners.length-2; i>=0; i-=2) {
            if (listeners[i]==FloatChangeListener.class) {
            	
                ((FloatChangeListener)listeners[i+1]).floatChanged();
            }          
        }
    }

