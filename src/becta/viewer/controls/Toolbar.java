	/**
	 * To rese the controls like menu , volume panel etc
	 */
	public void reset(){		
	
		//Reset menus 
		accessibilityMenu = null;
		volumePanel = null;
		if(annotationDialog!=null){
			annotationDialog.setVisible(false);
			annotationDialog = null;
		}
		
		//Rest the button
		btnZoom.setIcon(new ImageIcon(Toolbar.class.getResource("resources/button_zoom_in_tool.png")));
		btnZoom.setActionCommand("zoom_plus");
		
		//Reset toolbar
		if(this.isFloating())
			((ToolbarUI)getUI()).setFloating(false, new Point(1,1));
		
		//In our case , the toolbar needs to docked to the south
		Container dockingSource = getParent();
		dockingSource.remove(this);
		setOrientation(JToolBar.HORIZONTAL);
		dockingSource.add(BorderLayout.SOUTH, this);
	    dockingSource.invalidate();
 	    Container dockingSourceParent = dockingSource.getParent();
	    if (dockingSourceParent != null) 
	    	dockingSourceParent.validate();
	    dockingSource.repaint();
	    
	    //Reset the modes
	    Viewer viewer = Viewer.getViewer();
	    viewer.resetZoom();
	    viewer.setMute(false);
	    viewer.setVolume(50);
	    
	    if(Platform.isWindows)
	    	ThemeManager.setTheme("System Settings");
	    else
	    	ThemeManager.setTheme("Default");
		
		//Reset revealer
		if(viewer.getActiveFrame() != null){
			
			Revealer revealer = viewer.getActiveFrame().getScrollPane().getRevealer();
			revealer.setInitialPosition(SwingConstants.CENTER, 0);
			if(revealer.isVisible())
				viewer.toggleRevealer();
		}
		
		
	}
	
