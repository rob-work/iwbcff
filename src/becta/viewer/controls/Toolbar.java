	/**
	 * Sets the accessibility properties for the component	 * 
	 */
	private void setAccessibility(JComponent comp,String key){
		
		AccessibleContext ac =  comp.getAccessibleContext();
		ac.setAccessibleName(resourceBundle.getString(key+".AccessibleName"));
		ac.setAccessibleDescription(resourceBundle.getString(key+".AccessibleDescription"));
		comp.setToolTipText(resourceBundle.getString(key+".TooltipText"));
	}

	/**
	 * Calculates the location for the popup window
	 * @return Point value indicating the location
	 */
	private Point getPopupLocation(JButton invoker,Container popup){
		
		Point p = null;
		if(isFloating())
		{	
			if(invoker == btnVolume || invoker == btnZoom){				
				p = invoker.getLocationOnScreen();
				p.y -= popup.getPreferredSize().height;
				if(invoker == btnVolume)
					p.y -= invoker.getHeight();
			}
			else{
				p = invoker.getParent().getLocationOnScreen();
				p.y -=popup.getPreferredSize().height;				
			}
				
		}
		else if( this.getOrientation() == HORIZONTAL){
			p = invoker.getLocationOnScreen();
			p.y -= popup.getPreferredSize().height;
		}
		else{	
			p = invoker.getLocationOnScreen();
			//Docked to left
			if(this.getX() < 50){				
				p.x = getWidth();				
			}
			else{
				p.x -= popup.getPreferredSize().width-10;
			}
		}
		
		return p;
	}

	/**
	 * Method to create accessibility menu item	 * 
	 */
	private JRadioButtonMenuItem createMenuItem(String text,Color bg,Color fg,Color b,Border border){
		
		JRadioButtonMenuItem item = new JRadioButtonMenuItem(text);
		item.setBackground(bg);
		item.setForeground(fg);
		item.setBorder(border);
		item.setUI(new AccessibleMenuUI(b));
		item.addActionListener(this);
		item.setFont(ThemeManager.getTheme().getFont("ViewerControlFont"));
		setAccessibilityMenuItemSize(item, text);
		return item;
	}
	
	/**
	 * Method to create accessibility menu * 
	 */
	private JPopupMenu createAccessibilityMenu(){
		
		if(accessibilityMenu==null){					
			
			accessibilityMenu = new JPopupMenu();	
					    
		   //Add support for left/right key button
    	   Action actionListener = new AbstractAction() { 
			      public void actionPerformed(ActionEvent actionEvent) { 
			    	  MenuElement[] selectionPath =  MenuSelectionManager.defaultManager().getSelectedPath();
			    	  if(selectionPath!=null && selectionPath.length == 2){
			    		  
			    		  JRadioButtonMenuItem item = (JRadioButtonMenuItem)selectionPath[1];
			    		  int index = item.getParent().getComponentZOrder(item);
			    		  int count = item.getParent().getComponentCount();
			    		  int midCount = count / 2;
			    		  index += index < midCount ? midCount:-midCount;
			    		  if(index > 0 && index < count){
			    			  selectionPath[1] = (MenuElement)item.getParent().getComponent(index);
			    			  MenuSelectionManager.defaultManager().setSelectedPath(selectionPath);
			    		  }		    			  
			    	  }
			      } 
		    } ;
		    
		    InputMap inputMap = accessibilityMenu.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW);
		    inputMap.put( KeyStroke.getKeyStroke("LEFT"), "LEFT_RIGHT");
		    inputMap.put( KeyStroke.getKeyStroke("RIGHT"), "LEFT_RIGHT");
		    
		    accessibilityMenu.getActionMap().put("LEFT_RIGHT", actionListener);
		    
			ButtonGroup group = new ButtonGroup();
			Border 	menuBorder = BorderFactory.createEmptyBorder(1, 1, 1, 1);
			
			accessibilityMenu.setBackground(AccessibilityColor.window);
			
			JLabel header = new JLabel(resourceBundle.getString("menuAccessibility.Title"));
			AccessibleContext ac =  header.getAccessibleContext();
			ac.setAccessibleName(resourceBundle.getString("menuAccessibility.Title.AccessibleName"));
			ac.setAccessibleDescription(resourceBundle.getString("menuAccessibility.Title.AccessibleDescription"));
			
			header.setBackground(AccessibilityColor.window);			
			accessibilityMenu.add(header);
			
						//Adds menu items to accessibilityMenu for all available themes
			for (Theme theme : ThemeManager.getAllThemes()) {

				JRadioButtonMenuItem item =createMenuItem(theme.getName(),theme.getColor(AccessibilityColor.CONTROL),theme.getColor(AccessibilityColor.CONTROL_TEXT), theme.getColor(AccessibilityColor.CONTROL_DK_SHADOW),menuBorder);
				item.setActionCommand(theme.getName());
				group.add(item);
				item.setSelected(theme.isDefaultTheme());
				accessibilityMenu.add(item);
			}	
			
			accessibilityMenu.setLayout(new GridLayout( ((int)Math.ceil((accessibilityMenu.getComponentCount()-1 )/(float)2 ))  +1 ,2,4,4)
			{
					@Override
					public void layoutContainer(Container parent) {
						
						synchronized (parent.getTreeLock()) {
				        	
				        	Insets insets = new Insets(4,4,4,4);
				        	
				        	int ncomponents = parent.getComponentCount();
				        	int nrows = getRows();
				        	int ncols = getColumns();
				        	

				        	if (ncomponents == 0) {
				        	    return;
				        	}
				        	if (nrows > 0) {
				        	    ncols = (ncomponents + nrows - 1) / nrows;
				        	} else {
				        	    nrows = (ncomponents + ncols - 1) / ncols;
				        	}
				        	int w = parent.getWidth() - (insets.left + insets.right);
				        	int h = parent.getHeight()- (insets.top + insets.bottom);
				        	w = (w - (ncols - 1) * getHgap()) / ncols;
				        	h = (h - (nrows - 1) * getVgap()) / nrows;
				        	
				        	int i = 0;
				    	    for (int c = 0, x = insets.left ; c < ncols ; c++, x += w + getHgap()) {
				    		for (int r = 0, y = insets.top ; r < nrows ; r++, y += h + getVgap()) {
				    			if(i==0)
				    			{
				    				parent.getComponent(i).setBounds(x+ getHgap(), y, w*ncols, h);
				    				insets.top+=h + getVgap();
				    				
				    			}
				    			else if (i < ncomponents) 
				    		    {
				    		    	parent.getComponent(i).setBounds(x, y, w, h);
				    		    }
				    		    ++i;
				    		}
				    	    }
				         }
					}
			}
			);
		}
		return accessibilityMenu;
		
	}	
	
